package day14_forLoop;

public class ForLoopIntro {

    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("------------------------------------------");

        for(int i = 1; i <= 10; i++ ){ //if we want to print it 10 times put 11 or i<=10
            System.out.println("Hello World");

        }







    }
}
