package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class");

        System.out.println("-------------");

        System.out.println("\tJava Programming Language");

        System.out.println("\t\tJava Developer Course 1");
        System.out.println("\\"); //will show only 1 backslash
        System.out.println("My favourite TV show is \"The Walking Dead\"");

        /*
        1. \n: New line
        2. \t: Paragraph space
        3. \\: Back Slash
        4. \": double quote
         */



    }
}
